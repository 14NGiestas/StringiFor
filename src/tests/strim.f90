!< StringiFor `strim` test.
program strim
!-----------------------------------------------------------------------------------------------------------------------------------
!< StringiFor `strim` test.
!-----------------------------------------------------------------------------------------------------------------------------------
use, intrinsic :: iso_fortran_env, only : stdout => output_unit
use stringifor, only : string
!-----------------------------------------------------------------------------------------------------------------------------------

!-----------------------------------------------------------------------------------------------------------------------------------
implicit none
type(string) :: astring !< A string.
!-----------------------------------------------------------------------------------------------------------------------------------

!-----------------------------------------------------------------------------------------------------------------------------------
astring = 'Hello World!   '
write(stdout, "(A)") 'Original: "'//astring//'"'
write(stdout, "(A)") 'Trimmed:  "'//astring%strim()//'"'
stop
!-----------------------------------------------------------------------------------------------------------------------------------
endprogram strim
